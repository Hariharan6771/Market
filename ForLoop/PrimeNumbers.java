package ForLoop;

public class PrimeNumbers {
	public static void main(String[] args) {
	int num=7;
	int count=0;
	for(int i=2;i<7;i++) {
		count=count+1;
	}
	if(num%count==0) {

	
		System.out.println(count+" "+"Not Prime number");
	}
	
	
    else {
		System.out.println(count+" "+"Is a prime number");
    }
}
}
	
	

	
	
	

	
	
